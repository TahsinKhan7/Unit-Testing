import math

def find_sqrt(num):
    return math.sqrt(num)

def find_ceil(num):
    return math.ceil(num)

def adding(num1, num2):
    return num1 + num2

def subtracting(num1, num2):
    return num1 - num2

def multiplying(num1, num2):
    return num1 * num2

def dividing(num1, num2):
    return num1 / num2